#list append function
animals=['dog', 'cat', 'lion', 'tiger', 'rabbit']
animals.append('bear')
print('new list=',animals)

#list copy function
animals=['dog', 'cat', 'lion', 'tiger', 'rabbit']
print("old list is....", animals)
new_list=animals.copy()
print("new list is....", new_list)

#list index function
animals=['dog', 'cat', 'lion', 'tiger', 'rabbit']
print("The index of lion is....", animals.index('lion'))

#list remove function
animals=['dog', 'cat', 'lion', 'tiger', 'rabbit']
animals.remove('cat')
print('new list is...' , animals)

#list insert function
animals=['dog', 'cat', 'lion', 'tiger', 'rabbit']
animals.insert(3, 'cow')
print("new list is...", animals)





